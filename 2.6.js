let jour=Number(prompt("Enter a number between 1 and 7"));

if(jour==1){
    console.log("Monday")
}
else if(jour==2){
    console.log("Tuesday")
}

else if(jour==3){
    console.log("Wednesday")
}
else if(jour==4){
    console.log("Thursday")
}
else if(jour==5){
    console.log("Friday")
}
else if(jour==6){
    console.log("Saturday")
}

else if(jour==7){
    console.log("Sunday")
}
else{
    console.log("It's not a number between 1 and 7")
}